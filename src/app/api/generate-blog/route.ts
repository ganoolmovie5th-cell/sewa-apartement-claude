// POST — dipanggil manual dari blog page (tombol Generate)
// ─────────────────────────────────────────────────────────────────────────────
export async function POST(req: NextRequest) {
  const authHeader = req.headers.get("authorization");
  const cronSecret = process.env.CRON_SECRET || "sewa-cron-secret-2024";
  const isVercelCron = req.headers.get("x-vercel-cron") === "1";

  if (!isVercelCron && authHeader !== `Bearer ${cronSecret}`) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  try {
    const allArticles = getAllArticles();
    const weeksSinceEpoch = Math.floor(Date.now() / (7 * 24 * 60 * 60 * 1000));
    const topicIndex = weeksSinceEpoch % WEEKLY_TOPICS.length;
    const topic = WEEKLY_TOPICS[topicIndex];

    // Cek duplikat minggu ini
    const sevenDaysAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000);
    const alreadyExists = allArticles.some(
      (b) => b.topicId === topic.id && new Date(b.publishedAt) > sevenDaysAgo
    );
    if (alreadyExists) {
      return NextResponse.json({
        success: false,
        message: `Artikel topik "${topic.title.id}" sudah ada minggu ini.`,
      });
    }

    // Generate dengan Mistral (atau fallback)
    const generated = await generateWithMistral(topic);

    const now = new Date();
    const slug = `${slugify(generated.title_id)}-${now.toISOString().split("T")[0]}`;

    const newArticle = {
      id: `auto-${Date.now()}`,
      slug,
      topicId: topic.id,
      title: { id: generated.title_id, en: generated.title_en },
      excerpt: { id: generated.excerpt_id, en: generated.excerpt_en },
      content: { id: generated.content_id, en: generated.content_en },
      category: topic.category,
      tags: generated.tags || topic.keywords,
      image: topic.image,
      author: "AI Redaksi SewaApartement",
      authorAvatar: "https://images.unsplash.com/photo-1485827404703-89b55fcc595e?w=60&q=80",
      publishedAt: now.toISOString(),
      readTime: Math.max(3, Math.ceil((generated.content_id?.length || 1500) / 1200)),
      views: 0,
      featured: false,
      autoGenerated: true,
      generatedWithAI: !!process.env.MISTRAL_API_KEY,
    };

    // Simpan ke in-memory cache
    addToCache(newArticle);

    return NextResponse.json({
      success: true,
      message: `✅ Artikel berhasil dibuat: "${newArticle.title.id}"`,
      article: {
        id: newArticle.id,
        slug: newArticle.slug,
        title: newArticle.title,
        publishedAt: newArticle.publishedAt,
        generatedWithAI: newArticle.generatedWithAI,
      },
      // Kirim artikel lengkap agar frontend bisa simpan ke localStorage
      fullArticle: newArticle,
    });
  } catch (err: any) {
    console.error("[generate-blog POST] Error:", err);
    return NextResponse.json(
      { success: false, error: err.message || "Internal server error" },
      { status: 500 }
    );
  }
}

