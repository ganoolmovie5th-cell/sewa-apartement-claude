declare global {
  var __blogCache: any[] | undefined;
}

function getCache(): any[] {
  if (!global.__blogCache) global.__blogCache = [];
  return global.__blogCache;
}

function addToCache(article: any) {
  const cache = getCache();
  // Hindari duplikat
  if (!cache.find((a) => a.id === article.id)) {
    cache.push(article);
  }
}

